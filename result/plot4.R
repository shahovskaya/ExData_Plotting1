if(!file.exists('data.zip')){
  url<-"https://d396qusza40orc.cloudfront.net/exdata%2Fdata%2Fhousehold_power_consumption.zip"
  download.file(url,destfile = "data.zip")
}
unzip("data.zip")
newdata01 <- subset(read.table("household_power_consumption.txt", header = TRUE, sep = ";"), Date=="1/2/2007")
newdata02 <- subset(read.table("household_power_consumption.txt", header = TRUE, sep = ";"), Date=="2/2/2007")
total <- rbind(newdata01, newdata02)
total$DateTime<-paste(total$Date, total$Time)
data$DateTime<-strptime(data$DateTime,"%d/%m/%Y %H:%M:%S")
library(ggplot2)
labels <- c("Sub_metering_1","Sub_metering_2","Sub_metering_3")
columnlines <- c("black","red","blue")
par(mfrow=c(2,2))
plot(total$DateTime, total$Global_active_power, type="l", xlab="", ylab="Global Active Power")
plot(total$DateTime, total$Voltage, type="l", xlab="datetime", ylab="Voltage")
plot(total$DateTime, total$Sub_metering_1, type="l", xlab="", ylab="Energy sub metering")
lines(total$DateTime, total$Sub_metering_2, type="l", col="red")
lines(total$DateTime, total$Sub_metering_3, type="l", col="blue")
legend("topright", bty="n", legend=labels, col=c("black", "red", "blue"), lty=c(1,1,1))
plot(total$DateTime, total$Global_reactive_power, type="l", xlab="datetime", ylab="Global_reactive_power")
ggsave("plot4.png", width=127, height=127, units="mm")